    private void bTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTambahActionPerformed
        try {
            product prut = new product();
            category cty = new category();
            prut.setProduct_id(Integer.parseInt(tID.getText()));
            prut.setProduct_name(tNama.getText());
            prut.setProduct_desc(tDeskripsi.getText());
            prut.setProduct_price(Integer.parseInt(tHarga.getText()));
            cty.setCategory_name(cKategori.getSelectedItem().toString());
            ResultSet data = cty.konversi();
           
            if(data.next()){
                String isi = data.getString("category_id");
                prut.setProduct_cat_id(Integer.parseInt(isi));
            }
            prut.tambahProduct();
        } catch (SQLException sQLException) {
            System.out.println("kategori tidak dapat masuk");
        }
        loadTable();
        reset();
    }//GEN-LAST:event_bTambahActionPerformed

