     void loadTable(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Product ID");
        model.addColumn("Product");
        model.addColumn("Deskripsi");
        model.addColumn("Harga");
        model.addColumn("Kategori");
        
        try {
            product prot = new product();
            ResultSet data = prot.tampilProduct();
            
           while(data.next()){
               model.addRow(new Object[]{
                   data.getInt("ID"),
                   data.getString("Nama"),
                   data.getString("Deskripsi"),
                   data.getString("Harga"),
                   data.getString("Kategori")
               
               });
            }
           
        } catch (SQLException sQLException) {
            
        }
        
        tProduct.setModel(model);
    }
    
    void autoID(){
        try {
            product prot = new product();
            ResultSet id = prot.autoID();

            if (id.next()) {
                int auto = id.getInt("product_id") + 1;
                tID.setText(String.valueOf(auto));
            } else {
                tID.setText("1");
            }
        } catch (SQLException sQLException) {
        }
    }
    
    void comBox(){
        try {
            category cty = new category();
            ResultSet data = cty.tampilCombok();
            
            while (data.next()){
                String isi = data.getString("category_name");
                cKategori.addItem(isi);
            }
        } catch (SQLException sQLException) {
        }
        
        
    }
    
    void reset(){
        autoID();
        tID.setEditable(true);
        tDeskripsi.setText(null);
        tHarga.setText(null);
        cKategori.setSelectedItem(null);
        tNama.setText(null);
       
    }

