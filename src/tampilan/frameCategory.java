    private void tCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tCategoryMouseClicked
      
        int baris = tCategory.rowAtPoint(evt.getPoint());
        String ID = tCategory.getValueAt(baris, 0).toString();
        String Name = tCategory.getValueAt(baris, 1).toString();
        
        tCategoryID.setText(ID);
        tCategoryID.setEditable(false);
        tCategoryID.setVisible(true);
        tCategoryNAME.setText(Name);
    }//GEN-LAST:event_tCategoryMouseClicked

