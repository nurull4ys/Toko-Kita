    void loadTable(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Category ID");
        model.addColumn("Category Name");
        
        
        try {
            category ct = new category();
            ResultSet data = ct.tampilCategory();
            
           while(data.next()){
               model.addRow(new Object[]{
                   data.getInt("category_id"),
                   data.getString("category_name")
               
               });
            }
           
        } catch (SQLException sQLException) {
            
        }
        
        tCategory.setModel(model);
    }
    
    void autoID(){
        try {
            category cty = new category();
            ResultSet id = cty.autoID();

            if (id.next()) {
                int auto = id.getInt("category_id") + 1;
                tCategoryID.setText(String.valueOf(auto));
            } else {
                tCategoryID.setText("1");
            }
        } catch (SQLException sQLException) {
        }
    }
     void reset(){
        autoID();
        tCategoryID.setEditable(false);
        tCategoryNAME.setText(null);
       
    }

